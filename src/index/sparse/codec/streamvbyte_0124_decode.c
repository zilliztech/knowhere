#ifdef __SSE4_1__
typedef __m128i decode_t;

static inline decode_t
svb_decode_uint32x4(const uint8_t key, const uint8_t* __restrict__* dataPtrPtr) {
    uint8_t len = 0;
    decode_t Data = _mm_loadu_si128((const decode_t*)*dataPtrPtr);
    uint8_t* pshuf = (uint8_t*)&shuffleTable[key];
    decode_t Shuf = *(__m128i*)pshuf;
    len = lengthTable[key];
    Data = _mm_shuffle_epi8(Data, Shuf);
    *dataPtrPtr += len;
    return Data;
}

static inline void
svb_write_uint32x4(uint32_t* out, decode_t Vec) {
    _mm_storeu_si128((__m128i*)out, Vec);
}
#endif  // __SSE4_1__

#ifdef __ARM_NEON__
typedef uint8x16_t decode_t;

static inline decode_t
svb_decode_uint32x4(const uint8_t key, const uint8_t* __restrict__* dataPtrPtr) {
    uint8_t len = 0;
    uint8_t* pshuf = (uint8_t*)&shuffleTable[key];
    uint8x16_t decodingShuffle = vld1q_u8(pshuf);

    uint8x16_t compressed = vld1q_u8(*dataPtrPtr);
    len = lengthTable[key];
    uint8x16_t data = vqtbl1q_u8(compressed, decodingShuffle);
    *dataPtrPtr += len;
    return data;
}

static inline void
svb_write_uint32x4(uint32_t* out, decode_t Vec) {
    vst1q_u8((uint8_t*)out, Vec);
}
#endif  // __ARM_NEON__

static inline uint32_t
svb_decode_data(const uint8_t** dataPtrPtr, uint8_t code) {
    const uint8_t* dataPtr = *dataPtrPtr;
    uint32_t val;

    if (code == 0) {  // 0 byte
        val = 0;
    } else if (code == 1) {  // 1 bytes
        val = (uint32_t)*dataPtr;
        dataPtr += 1;
    } else if (code == 2) {  // 2 bytes
        val = 0;
        memcpy(&val, dataPtr, 2);  // assumes little endian
        dataPtr += 2;
    } else {  // code == 3, 4 bytes
        memcpy(&val, dataPtr, 4);
        dataPtr += 4;
    }

    *dataPtrPtr = dataPtr;
    return val;
}

static const uint8_t*
svb_decode_scalar(uint32_t* outPtr, const uint8_t* keyPtr, const uint8_t* dataPtr, uint32_t count) {
    if (count == 0) {
        return dataPtr;  // no reads or writes if no data
    }

    uint8_t shift = 0;
    uint32_t key = *keyPtr++;
    for (uint32_t c = 0; c < count; c++) {
        if (shift == 8) {
            shift = 0;
            key = *keyPtr++;
        }
        uint32_t val = svb_decode_data(&dataPtr, (key >> shift) & 0x3);
        *outPtr++ = val;
        shift += 2;
    }

    return dataPtr;  // pointer to first unused byte after end
}

